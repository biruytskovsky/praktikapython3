def number_to_words(number):
    # Словари для текстового представления чисел
    ones = {
        0: '', 1: 'один', 2: 'два', 3: 'три', 4: 'четыре', 5: 'пять',
        6: 'шесть', 7: 'семь', 8: 'восемь', 9: 'девять'
    }
    teens = {
        10: 'десять', 11: 'одиннадцать', 12: 'двенадцать', 13: 'тринадцать',
        14: 'четырнадцать', 15: 'пятнадцать', 16: 'шестнадцать', 17: 'семнадцать',
        18: 'восемнадцать', 19: 'девятнадцать'
    }
    tens = {
        2: 'двадцать', 3: 'тридцать', 4: 'сорок', 5: 'пятьдесят',
        6: 'шестьдесят', 7: 'семьдесят', 8: 'восемьдесят', 9: 'девяносто'
    }
    hundreds = {
        1: 'сто', 2: 'двести', 3: 'триста', 4: 'четыреста',
        5: 'пятьсот', 6: 'шестьсот', 7: 'семьсот', 8: 'восемьсот', 9: 'девятьсот'
    }

    if number == 0:
        return 'ноль'
    elif number < 10:
        return ones[number]
    elif number < 20:
        return teens[number]
    elif number < 100:
        return tens[number // 10] + ' ' + ones[number % 10]
    elif number < 1000:
        if number % 100 == 0:
            return hundreds[number // 100]
        else:
            return hundreds[number // 100] + ' ' + number_to_words(number % 100)
    else:
        return 'Текстовое представление не поддерживается для чисел больше 1000'

# Пример использования
while True:
    try:
        number = int(input("Введите число от 1 до 1000: "))
        if 1 <= number <= 1000:
            print("Текстовое представление числа:", number_to_words(number))
            break
        else:
            print("Число должно быть от 1 до 1000.")
    except ValueError:
        print("Ошибка: Введите корректное целое число.")